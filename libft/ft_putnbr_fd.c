#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		del;
	char	c;

	del = 1;
	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	while (n / del > 9)
		del *= 10;
	while (del > 0)
	{
		c = n / del + '0';
		write(fd, &c, 1);
		n %= del;
		del /= 10;
	}
}
